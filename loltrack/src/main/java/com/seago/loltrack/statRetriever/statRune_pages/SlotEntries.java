
package com.seago.loltrack.statRetriever.statRune_pages;


class SlotEntries{
   	private Rune rune;
   	private Number runeId;
   	private Number runeSlotId;

 	public Rune getRune(){
		return this.rune;
	}
	public void setRune(Rune rune){
		this.rune = rune;
	}
 	public Number getRuneId(){
		return this.runeId;
	}
	public void setRuneId(Number runeId){
		this.runeId = runeId;
	}
 	public Number getRuneSlotId(){
		return this.runeSlotId;
	}
	public void setRuneSlotId(Number runeSlotId){
		this.runeSlotId = runeSlotId;
	}
}
